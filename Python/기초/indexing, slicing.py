###############   Indexing   ###############
a = 'Life is too short. You need python'

a[0] # 'L' indexing의 첫 번째 자리 0
a[12] # 's'
a[-1] # 'n' 마지막은 -기호를 붙여서 -1
a[-2] # 'o'



###############   Slicing   ###############
b = 'Hello'

b[0:5] # 'Hello'
b[:5] # 'Hello'
# 0과 빈칸은 첫 번째 자리를 의미함
b[:3] # 'Hel'
# 0 <= b < 3이므로 0, 1, 2 3개만 출력

b[0] + b[1] + b[2] + b[3] + b[4] # indexing
b[:5] # slicing

c = '20231226snow'
c[:8]
c[-4:]




